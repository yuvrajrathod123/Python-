import math

# factorial function

print(math.factorial(2))
print(math.factorial(3))
print(math.factorial(4))

print("\n")
# ceil function

print(math.ceil(1.6))
print(math.ceil(0.6))
print(math.ceil(-2.4))

print("\n")

# floor function

print(math.floor(1.6))
print(math.floor(0.6))
print(math.floor(-2.4))

print("")
# sqrt function

print(math.sqrt(16))
print(math.sqrt(8))
print(math.sqrt(10))